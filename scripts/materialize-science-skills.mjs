function normalizeSkillName(value) {
  const normalized = value
    .normalize('NFKD')
    .replace(/[^\w\s.-]+/gu, ' ')
    .replace(/[_.\s]+/gu, '-')
    .replace(/-+/gu, '-')
    .replace(/^-|-$/gu, '')
    .toLowerCase();
  return normalized || 'skill';
}

function toPosix(relativePath) {
  return relativePath.split(path.sep).join('/');
}

function walkSkillFiles(dir) {
  const result = [];
  if (!fs.existsSync(dir)) return result;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      result.push(...walkSkillFiles(fullPath));
    } else if (entry.isFile() && entry.name === 'SKILL.md') {
      result.push(fullPath);
    }
  }
  return result.sort();
}

function parseFrontmatter(text) {
  if (!text.startsWith('---')) {
    return { attrs: {}, body: text };
  }
  const end = text.indexOf('\n---', 3);
  if (end < 0) {
    return { attrs: {}, body: text };
  }
  const raw = text.slice(3, end).trim();
  const body = text.slice(end + 4).replace(/^\r?\n/u, '');
  const attrs = {};
  for (const line of raw.split(/\r?\n/u)) {
    const match = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/u);
    if (!match) continue;
    attrs[match[1]] = stripYamlValue(match[2]);
  }
  const versionMatch = raw.match(/(?:^|\s)(?:version|"version"):\s*["']?([^"',\n}]+)/u);
  if (versionMatch) attrs.version = versionMatch[1].trim();
  return { attrs, body };
}

function stripYamlValue(value) {
  const trimmed = value.trim();
  if ((trimmed.startsWith('"') && trimmed.endsWith('"')) || (trimmed.startsWith("'") && trimmed.endsWith("'"))) {
    return trimmed.slice(1, -1);
  }
  return trimmed;
}

function yamlString(value) {
  return JSON.stringify(String(value).replace(/\s+/gu, ' ').trim());
}

function truncateDescription(value) {
  const cleaned = String(value || '')
    .replace(/\s+/gu, ' ')
    .trim();
  if (cleaned.length <= 900) return cleaned;
  return `${cleaned.slice(0, 860).replace(/\s+\S*$/u, '')}...`;
}

function hasExecutableFiles(dir) {
  let found = false;
  walkFiles(dir, (filePath) => {
    if (found) return;
    const rel = toPosix(path.relative(dir, filePath));
    if (rel === 'SKILL.md') return;
    if (rel.startsWith('scripts/') || /\.(?:py|sh|bash|zsh|js|mjs|ts|tsx|r|R|jl|pl|rb|lua|ipynb)$/u.test(rel)) {
      found = true;
    }
  });
  return found;
}

function walkFiles(dir, visitor) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === '.git' || entry.name === 'node_modules' || entry.name === '__pycache__') continue;
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walkFiles(fullPath, visitor);
    } else if (entry.isFile()) {
      visitor(fullPath);
    }
  }
}

function classifyDomains(text) {
  const lower = text.toLowerCase();
  const tags = [];
  for (const [tag, pattern] of DOMAIN_RULES) {
    if (pattern.test(lower)) tags.push(tag);
  }
  return tags.length ? tags : ['general-science'];
}

function classifyRisk({ text, hasScripts, domainTags }) {
  const lower = text.toLowerCase();
  if (/\b(api key|token|oauth|credential|secret|aws|gcp|azure|benchling|dnanexus|latchbio)\b/u.test(lower)) {
    return 'credential';
  }
  if (domainTags.includes('remote-compute')) return 'external_compute';
  if (hasScripts || /\b(write|delete|install|pip install|conda install|uv pip)\b/u.test(lower)) return 'write';
  if (/\b(http|rest api|endpoint|download|upload|network|search)\b/u.test(lower)) return 'network';
  return 'low';
}

function classifyClinicalBoundary(id, originalName, originalDescription) {
  const scope = `${id}\n${originalName}\n${originalDescription}`.toLowerCase();
  return /\b(clinical[- ]decision|clinical[- ]reports?|clinical workflow|patient[- ](?:care|facing|data|record|cohort|level|specific)|patients|treatment[- ]plans?|medical device|iso[- ]13485|pacs|dicom|pydicom|pyhealth|healthcare)\b/u.test(
    scope
  )
    ? 'medical_evidence_required'
    : 'none';
}

function classifyExecutionPolicy({ packId, hasScripts, risk, domainTags, id }) {
  if (hasScripts) return 'quarantined_script';
  if (id === 'kdense-database-lookup' || id === 'kdense-paper-lookup' || id === 'kdense-citation-management') {
    return 'active_default';
  }
  if (id === 'aer-auto-empirical-research-skills') return 'active_default';
  if (packId === 'deepscientist') return 'active_default';
  if (domainTags.includes('clinical')) return 'restricted_default';
  if (domainTags.includes('lab-integration') || domainTags.includes('remote-compute')) return 'restricted_default';
  if (risk === 'credential' || risk === 'external_compute') return 'restricted_default';
  return 'available_default';
}

function priorityFor(source, policy, id, domainTags) {
  if (id === 'ds-science') return 95;
  if (id === 'aer-auto-empirical-research-skills') return 88;
  if (source.packId === 'deepscientist') return policy === 'quarantined_script' ? 70 : source.defaultPriority;
  if (source.packId === 'autoEmpirical' && id.includes('statspai')) return 78;
  if (source.packId === 'autoEmpirical' && id.includes('full-empirical')) return 76;
  if (source.packId === 'autoEmpirical' && id.includes('paper-workflow')) return 72;
  if (source.packId === 'autoEmpirical' && domainTags.includes('causal-inference')) return 64;
  if (source.packId === 'autoEmpirical' && domainTags.includes('econometrics')) return 62;
  if (domainTags.includes('database')) return 65;
  if (policy === 'restricted_default') return 25;
  if (policy === 'quarantined_script') return 45;
  return source.defaultPriority;
}

function generatedSkillDirNames() {
  const names = new Set();
  if (fs.existsSync(MANIFEST_PATH)) {
    const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
    for (const skill of manifest.skills || []) {
      if (skill?.id) names.add(skill.id);
    }
  }
  for (const entry of fs.readdirSync(SKILLS_ROOT, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    if (!/^(ds|kdense|aer|sciagent)-/u.test(entry.name)) continue;
    const skillPath = path.join(SKILLS_ROOT, entry.name, 'SKILL.md');
    if (fs.existsSync(skillPath) && fs.readFileSync(skillPath, 'utf8').includes(GENERATED_MARKER)) {
      names.add(entry.name);
    }
  }
  return [...names].sort();
}

function cleanGeneratedSkills() {
  for (const name of generatedSkillDirNames()) {
    const target = path.join(SKILLS_ROOT, name);
    if (!target.startsWith(SKILLS_ROOT + path.sep)) {
      throw new Error(`Refusing to delete unexpected generated skill path: ${target}`);
    }
    if (fs.existsSync(target)) fs.rmSync(target, { recursive: true, force: true });
  }
}

function copySkillDirectory(srcDir, destDir) {
  fs.cpSync(srcDir, destDir, {
    recursive: true,
    force: true,
    filter: (src) => {
      const base = path.basename(src);
      return base !== '.git' && base !== 'node_modules' && base !== '__pycache__' && base !== '.DS_Store';
    },
  });
}

function copyFileIfExists(src, dest) {
  if (!fs.existsSync(src)) return false;
  fs.mkdirSync(path.dirname(dest), { recursive: true });
  fs.copyFileSync(src, dest);
  return true;
}

function isLightweightRouterSkill(source, skillPath) {
  return source.packId === 'autoEmpirical' && path.dirname(skillPath) === source.root;
}

function copyLightweightRouterSkill(source, destDir) {
  fs.mkdirSync(destDir, { recursive: true });
  const referencesDir = path.join(destDir, 'references');
  fs.mkdirSync(referencesDir, { recursive: true });
  copyFileIfExists(path.join(source.root, 'catalog', 'skills.json'), path.join(referencesDir, 'aers-skills.json'));
  copyFileIfExists(
    path.join(source.root, 'catalog', 'skills-enriched.json'),
    path.join(referencesDir, 'aers-skills-enriched.json')
  );
  copyFileIfExists(
    path.join(source.root, 'catalog', 'provenance.json'),
    path.join(referencesDir, 'aers-provenance.json')
  );
  for (const doc of ['TAXONOMY.md', 'SKILL_CATALOG.md', 'GOLDEN_WORKFLOWS.md', 'INSTALL.md']) {
    copyFileIfExists(path.join(source.root, 'docs', doc), path.join(referencesDir, doc));
  }
}

